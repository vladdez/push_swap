#include "libft.h"

void	ft_print_bits(unsigned char octet)
{
	int i;
	int a;

	i = 7;
	a = 0;
	while (i >= 0)
	{
		a = (octet >> i) & 1;
		a = a + '0';
		write(1, &a, 1);
		i--;
	}
}
